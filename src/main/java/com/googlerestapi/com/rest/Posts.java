package com.googlerestapi.com.rest;

public class Posts {
	public String id;
	public String title;
	public String author;
	
	
	public void setId(String id){
		this.id=id;
	}
	
	public void setAuthor(String author){
		this.author=author;
	}
	
	public void setTitle(String title){
		this.title=title;
	}
	
	
	public String getId(){
		return id;
	}
	
	public String getAuthor(){
		return author;
	}
	
	public String getTitle(){
		return title;
	}
	
	
	
	
	
	
	
	
}
